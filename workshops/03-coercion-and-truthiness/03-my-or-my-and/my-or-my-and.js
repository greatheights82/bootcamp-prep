const myOr = (arg1, arg2, arg3) => {
    if(arg1){
        return arg1;
    } else if(arg2){
        return arg2;
    } else return arg3; 
    
}

const myAnd = (arg1, arg2, arg3) => {
    if(!arg1){
        return arg1;
    } else if(!arg2){
        return arg2;
    } else return arg3; 
    
}